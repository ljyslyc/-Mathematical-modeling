% curve_filt_hand_buid.m
%% 清理
clear all
close all
clc

%%
% 输入
x=-9:8;

% 期望输出;
y=[129,-32,-118,-138,-125,-97,-55,-23,-4,...
    2,1,-31,-72,-121,-142,-174,-155,-77];
% 隐含节点的中心
t=x;

% 计算每一个输入到每一个中心的距离，作为隐含层的输入
z=dist(x',t);
% 计算隐含层的输出 
G=radbas(z);
%期望输出
d=y';
% 伪逆。求出权值向量
w=inv(G.'*G)*G.'*d;

%% 保存
save net.mat d w x y
